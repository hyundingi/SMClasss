
# 함수선언 - 기본매개변수 사용 --------------------------------------------
def func(num1, num2):
  print(num1)
  print(num2)

# 함수호출
# 에러 : func(10) / func(10,20,30)
func(10,20)

# 함수는 매개변수 개수를 정확히 맞춰야 에러가 나지 않음

# 함수선언 - 가변매개변수 사용 --------------------------------------------
def func2(*num):   # 튜플타입
  print(num)
  print(len(num))

# 함수호출
func2(10)  
func2(10,2,3,4,5,6)

# 가변매개변수는 개수를 알아서 맞춰 출력해줌 (튜플타입)